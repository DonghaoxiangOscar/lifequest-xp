function getNestedValue(source, key) {
  return key.split(".").reduce((current, part) => current?.[part], source);
}

function interpolate(template, params = {}) {
  if (typeof template !== "string") return template;

  return template.replace(/\{(\w+)\}/g, (_, key) => {
    return Object.prototype.hasOwnProperty.call(params, key) ? String(params[key]) : `{${key}}`;
  });
}

export function translate(language, key, params) {
  const selectedLanguage = translations[language] ? language : "en";
  const template = getNestedValue(translations[selectedLanguage], key) ?? getNestedValue(translations.en, key) ?? key;
  return interpolate(template, params);
}
